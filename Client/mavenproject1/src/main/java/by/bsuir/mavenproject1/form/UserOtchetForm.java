    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
 
        double [] mass = new double [12];
        double resultSum = 0;
        ServerConnection ser = new ServerConnection();
        int month = 1;
        List<MinusOrPlus> list;
        ObjectMapper obj = new ObjectMapper();
        for(int i=0;i<12;i++)
        {
            resultSum = 0;
             String node = ser.sendRequestGetSting("load-minus-plus", "{\"id\":"+user.getId()+",\"mess\":"+month+"}");
            try {
                list = Arrays.asList(obj.readValue(node, MinusOrPlus[].class));
                for(MinusOrPlus en:list)
                {
                    if(en.getMinusOrPlus().equals("minus"))
                    {
                    resultSum = resultSum - Double.parseDouble(en.getSumm());
                  
                    }else{
                        resultSum = resultSum + Double.parseDouble(en.getSumm());
                    }
                 
                }
            } catch (JsonProcessingException ex) {
                Logger.getLogger(UserOtchetForm.class.getName()).log(Level.SEVERE, null, ex);
            }
             
             mass[i] = resultSum;
             month++;
        }
         JFreeChart chart = createChart(Grafik.createDataset(mass));
    chart.setPadding(new RectangleInsets(50, 50, 50, 50));
    chart.createBufferedImage(100, 100);
    
    ChartPanel panel = new ChartPanel(chart);
    panel.setVisible(true);
    JFrame frame = new JFrame();
   
    frame.getContentPane().add(panel);          
    frame.setPreferredSize(new Dimension(800, 500));
          
    frame.pack();
    frame.setVisible(true);

    }//GEN-LAST:event_jButton4ActionPerformed

