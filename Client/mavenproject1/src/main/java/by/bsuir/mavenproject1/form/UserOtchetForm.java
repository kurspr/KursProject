    public void clearTable(JTable table) {
   for (int i = 0; i < table.getRowCount(); i++) {
      for(int j = 0; j < table.getColumnCount(); j++) {
          table.setValueAt("", i, j);
      }
   }
}
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
            
        String mess = jTextField1.getText();
        if(mess.equals(""))
        {
            JOptionPane.showMessageDialog(null, "Введите месяц");
            return;
        }
        double resultSum = 0;
        ServerConnection ser = new ServerConnection();
        String node = ser.sendRequestGetSting("load-minus-plus", "{\"id\":"+user.getId()+",\"mess\":"+mess+"}");
          
                ObjectMapper obj = new ObjectMapper();
            try {
                
                minusOrPlusList = Arrays.asList(obj.readValue(node, MinusOrPlus[].class));
                jTable1.clearSelection();
                
                jTable2.clearSelection();
                this.clearTable(jTable1);
                this.clearTable(jTable2);
                jTable1.clearSelection();
                jTable2.clearSelection();
                int indexMinus =0;
                int indexPlus = 0;
                for(MinusOrPlus en:minusOrPlusList)
                {
                    if(en.getMinusOrPlus().equals("minus"))
                    {
                    jTable1.setValueAt(en.getName(), indexMinus, 0);
                    jTable1.setValueAt(en.getSumm(), indexMinus, 1);
                    indexMinus++;
                    }else{
                        jTable2.setValueAt(en.getName(), indexPlus , 0);
                        jTable2.setValueAt(en.getSumm(), indexPlus , 1);
                        indexPlus++;
                    }
                  
                   
                }
                
                for(MinusOrPlus en:minusOrPlusList)
                {
                    if(en.getMinusOrPlus().equals("minus"))
                    {
                    resultSum = resultSum - Double.parseDouble(en.getSumm());
                  
                    }else{
                        resultSum = resultSum + Double.parseDouble(en.getSumm());
                    }
                 
                }
                jLabel6.setText(resultSum+"");
                
            } catch (JsonProcessingException ex) {
                Logger.getLogger(BugalterEditor.class.getName()).log(Level.SEVERE, null, ex);
            }
     
    }//GEN-LAST:event_jButton2ActionPerformed

